  if a = Artist.create!(name: artist.fetch(:name))
    p "Artist '#{a.name}' created"
    artist.fetch(:albums).each do |album|
      if al = Album.create!(
        artist: a,
        name: album.fetch(:name),
        released_on: album.fetch(:released_on),
        artwork_url: album.fetch(:artwork_url)
      )
        p "Album '#{al.name}' created"
      end
    end
  end
end
